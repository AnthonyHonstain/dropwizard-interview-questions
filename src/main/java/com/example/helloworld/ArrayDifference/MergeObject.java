package com.example.helloworld.ArrayDifference;

public class MergeObject {
    private int value;
    private String source;

    public MergeObject(int value, String source) {
        this.value = value;
        this.source = source;
    }

    public int getValue() {
        return value;
    }

    public String getSource() {
        return source;
    }
}
